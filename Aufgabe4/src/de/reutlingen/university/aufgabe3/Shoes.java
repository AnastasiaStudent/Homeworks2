package de.reutlingen.university.aufgabe3;

public abstract class Shoes {
	private String name;

	public void setName(String value) {
		this.name = value;
	}

	public String getName() {
		return name;
	}

	private double schueGrosse;

	public void setSchueGrosse(double value) {
		this.schueGrosse = value;
	}

	public double getSchueGrosse() {
		return schueGrosse;
	}

}
